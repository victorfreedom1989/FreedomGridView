package com.freedom.gridview.bean;

public class Data {
	int id;
	int num;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public Data(int id, int num) {
		super();
		this.id = id;
		this.num = num;
	}

	public Data() {
		super();
	}

}
